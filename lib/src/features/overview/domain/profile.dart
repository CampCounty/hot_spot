class Profile {
  // Attribute
  String userID;
  String vorname;
  String nachname;
  int postleitzahl;
  String wohnort;
  String bundesland;
  String email;
  String geburtstag;

  Profile({
    required this.userID,
    required this.vorname,
    required this.nachname,
    required this.postleitzahl,
    required this.wohnort,
    required this.bundesland,
    required this.email,
    required this.geburtstag,
  });
}
